<h4>Let's build the 6 billion puzzle</h4>

<br />

<p>At the moment speaking, there are roughly 20.000.000 developers in the world. We are a LOT! so let's take advantage of it</p>
<p>So, generally speaking we can divide the world of programming in 100 domains, where a domain could be the c++ language</p>
<p>That means 200.000 developers per domain. </p>
<p>Imagine 200.000 developers adding only 1 solution per day, 300 days per year, only the first year.</p>
<p>That results in a total of 60.000.000 solutions. This compared with 600.000 questions asked on SO(for c++). There's a huge gap.</p>
<p>So in just 1 year we can do 100 times better then SO did in 10 years</p>

